<h1 style="font-family: helvetica; color: white;font-size: 30pt;" align="center">History of Our Institute</h1>
<div align="center"><image  src="pr2.jpg" style="height:400;width:400;"/></div>
<p>Sardar Vallabhbhai Patel Institute of Technology was established in 1997 with the ideals of Shri Sardar Vallabhbhai Patel.</p>
<p>The New English School Trust, Vasad, constituted the Board of Management of SVIT under the chairmanship of an educationist and administrator of Gujarat, Prof. Shantibhai M Amin, Shri.Shivakaka and Shri. Chandukaka.</p>
<p>SVIT houses an Aeronautical Engineering division as well, the only one in the State of Gujarat, with an intake of 60 students. It also has Architecture as a branch, called the College of Architecture.</p>
